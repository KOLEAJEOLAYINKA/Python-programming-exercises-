number = int(input("Enter your first number: "))
number2 = int(input("Enter your second number: "))
if number == number2:
    print(number, "and", number2, "are equal")

if number > number2:
    print(number, "is greater than", number2)

if number >= number2:
    print(number, "is greater than or equals to", number2)

if number < number2:
    print(number, "less than", number2)
if number <= number2:
    print(number, "less than or equals to", number2)

if number != number2:
    print(number, "and", number2, "are not equal")

